<p>
  Winter and spring of 2011 were considerably wet, and the slide was quite active during March and April.  The toe moved over 2.7 meters downslope and the central area (displayed on the web site) moved about 1.5 meters. <a href="images/RCC10_11.gif">2010-11 Summary Graph.</a>
</p>
<p>
  During the relatively dry winters of 2008-2009, 2007-2008, and 2006-2007, landslide movement was minimal.
</p>
<ul>
  <li><a href="images/RCC08_09.gif">2008-09 Summary Graph.</a>
  <li><a href="images/RCC07_08.gif">2007-08 Summary Graph.</a>
