</ul>
<p>
  During the very wet 2005-2006 season, movement in the central area of the slide (displayed on the web site) began in early March and continued to the beginning of May.  During this winter/spring period, both the central and toe areas of the slide moved more than 2 meters.  Velocities varied between 1 and 5 cm/day; higher velocities occurred during rainy periods. <a href="images/RCC05_06.gif">2005-06 Summary Graph.</a>
</p>
<p>
  In response to wet periods in December 2004 through May 2005, pore-water pressures within the slide were elevated.  During this wet season, the toe of the slide began creeping in late February 2005 and the central area began moving late March 2005.  Movement ceased by early June 2005.  During this time, the central area moved about 2 meters and the toe area moved over 4 meters downslope. <a href="images/RCC04_05.gif">2004-05 Summary Graph.</a>
</p>
<p>
  For the 2002-2003 wet season, downslope movement in the central and lower parts of the slide ceased in mid-May 2003.  Between mid-April and mid-May of 2003, the slide moved about 30 cm in the central part and about 160 cm in the lower part. <a href="images/RCC02_03.gif">2002-03 Summary Graph.</a>
</p>
<p>
  During the wet winter months of 2000, the slide moved a few centimeters. <a href="images/RCC99_00.gif">1999-2000 Summary Graph.</a>
</p>
<p>
  Between late January and early May 1999, downslope movement of about 30 centimeters (cm) occurred at this site near the center of the slide. <a href="images/RCC98_99.gif">1998-99 Summary Graph.</a> Downslope displacement also occurred in the upper and lower parts of this slide and ranged between about 20 and 120 cm.
</p>
<p>
  This slide also moved during the wet El Ni&ntilde;o-influenced winter. Between mid-March and late-June 1998, cumulative displacement near the center of the slide was about 65 cm. <a href="images/RCC97_98.gif">1997-98 Summary Graph.</a> Between late January and the end of June 1998, cumulative downslope movement at the toe of this slide was greater than 560 cm (18.4 feet).
</p>
