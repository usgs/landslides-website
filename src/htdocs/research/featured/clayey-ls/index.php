  During this time, they collected hourly measurements of rainfall, landslide movement, landslide thickness changes, groundwater pressure, and swell pressure (the pressure exerted when the soil in the landslide expands, usually due to the presence of water).  They also collected samples of the material (dirt) from the landslide to analyze in a laboratory.  From these samples they learned about the water content, the density, shear strength (how well the material holds together under pressure), type of landslide materials and minerals (most importantly the percentage of clay minerals), the shrink-swell amount, and the swell pressure.
</p>

<p>
  Previous studies had concluded that pore water pressure, or the pressure of water in the open spaces between the dirt particles in the landslide material, controlled the movement of earthflows. If that were the case, one would expect the landslide to start moving when it contained enough water, and to move the fastest when it contained the most water. However, the data in this study showed that the timing and speed of the earthflow movement did not correlate with the amount of water.   Instead, the initiation of movement was several months after the water amount peaked, and the peak speed occurred when the amount of water was decreasing.  The previously mentioned lab analyses of the samples from the earthflow were essential in illuminating the reasons for this behavior.
</p>

<figure>
<img src="images/graph-simplified.png" alt=""/>
<figcaption>
  This graph shows that the landslide movement (red) was slow during the times when there was more rainfall (green), the landslide was thicker (orange), and the swell pressure (gray) of the landslide material increased (both due to the clay expanding). The landslide movement increased when those 3 factors were decreasing.
</figcaption>
</figure>

<p>
  The earthflow material contained a large amount of expansive clay. Clay swells when it has more water and shrinks when there is less water.  The scientists hypothesized, from the correlation of the swell pressures measured in the laboratory and field with the earthflow movement, that the presence and amount of clay in the earthflow were connected to the timing of the movements and their speed. When there was a large volume of water present, the clay expanded and exerted high pressure on the sides of the landslide, restricting the movement of the earthflow like when brakes are applied to a wheel. Then when the water volume decreased, the clay shrunk, decreasing the swell pressure and allowing the earthflow to start moving again.
</p>

