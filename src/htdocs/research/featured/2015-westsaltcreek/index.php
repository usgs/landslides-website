<h3>Avalanche Insights from West Salt Creek</h3>

<p>
  The West Salt Creek avalanche provided a rare opportunity to collect and combine data that’s not usually available for avalanches.  The understanding of avalanche processes was greatly enhanced from the study of this avalanche, and the highlights are:
</p>

<ol>
  <li>
    An avalanche is a complex sequence of events, not just one massive slide.  Therefore if potential avalanche sites were monitored, precursory activity could help to alert residents below and allow time for evacuation.
  </li>
  <li>
    The various datasets and high-resolution measurements allowed scientists to suggest refinements to  models for avalanches, which should lead to better estimates of avalanche hazards.  Many avalanches that travel  great distances appear to have two layers: a more liquid underlying layer and a thicker and stronger layer that rides on top.
  </li>
  <li>
    Structures mapped in older landslides might be useful in determining the speed in which they moved. Knowing the speed that older landslides moved is important for assessing future landslide hazards.  Unfortunately, the distinctness of many avalanche structures degrades quickly (within 6 months at West Salt Creek) from weathering and vegetation.
  </li>
</ol>

