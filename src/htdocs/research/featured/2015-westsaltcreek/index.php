<h2>The West Salt Creek Rock Avalanche, Colorado, May 25, 2014</h2>

<figure class="right" style="width:450px">
  <img src="images/aerial.jpg" alt="aerial photo of avalanche"/>
  <figcaption>
  Aerial photo of the West Salt Creek rock avalanche (Photo courtesy of Mesa County Sheriff's Office)
  </figcaption>
</figure>

<p>
  Rock avalanches often occur in remote areas that are difficult to access, so eyewitness observations of an avalanche in action are rare.  Many avalanches are studied long after they occurred when much of the details of what happened have already been destroyed by natural weathering processes.  Exactly what happens during an avalanche, and why, has been difficult to determine.  However, this information is exactly what is needed to help scientists determine where future avalanches are more likely to occur and what the consequences could be.  This knowledge can help guide decisions that would have an impact on saving lives.
</p>
<p>
  On May 25, 2014 an unusual rainfall on top of melting snow triggered a series of geological events in the West Salt Creek Valley in western Colorado.  By the end of the day a volume of earth the size of a cube with sides about ¼ mile long had been transported a large distance of 4.6 km (think a 5K race, or 2.9 mi) from high on the side of the Grand Mesa down into the West Salt Creek Valley, killing 3 people.
</p>

<figure class="left" style="width:300px;">
  <img src="images/locationmap.gif" alt="location map" width="300"/>
  <figcaption>
  Map showing the location of the West Salt Creek rock avalanche in western Colorado.
  </figcaption>
</figure>

<p>
  In spite of this tragedy, the West Salt Creek Avalanche provided a unique opportunity to combine different types of data to perform a detailed analysis of what happened.  Scientists were able to use some new methods of reconstructing avalanches to figure out exactly what happened and when it happened.  First, there were rare eyewitness accounts of the avalanche, and the scientists were able to access the avalanche quickly before any evidence was destroyed.  They collected detailed data on the structure of the avalanche deposits, and they also used <a href="http://coloradogeologicalsurvey.org/geologic-mapping/lidar/">lidar</a> and detailed pre-avalanche maps that were available for the area.  Additionally, the avalanche was large enough to be recorded with great clarity on over 22 seismic stations. These factors provided data that are not always available in the study of an avalanche. The outcomes include a better understanding of how rock avalanches move and behave, and ideas on how to improve the numerical models used to study them.  Models are used to estimate the hazard of other landslides that may not have even happened yet, so better models yield better hazard information.
</p>

<h3>Eyewitness Accounts</h3>

<figure class="right" style="width:300px">
  <img src="images/downslope.jpg" alt=""/>
  <figcaption>
  Photograph taken from a Colorado National Guard helicopter looking north down the West Salt Creek rock avalanche deposit.  On May 25, 2014, the central core of the rock avalanche deposit continued to move for 1-2 hours after the main rock avalanche deposit had stopped moving.  Shallow landslides on the steep valley flanks continued for at least several days after the catastrophic failure on May 25. The length of the avalanche deposit visible is about 3.5 km. The width of the avalanche deposit in the foreground is about 500 m. (Photo credit: Jeff Coe, May 30, 2014)
  </figcaption>
</figure>

<p>
  Melvin &ldquo;Slug&CloseCurlyDoubleQuote; Hawkins describes the noise he heard early that morning as a &ldquo;strange hissing noise&CloseCurlyDoubleQuote;.  About 10 hours later in the early evening, the Bracco family heard a loud rumbling sound that rattled their windows. Different members of the family describe the noise like &ldquo;a low flying, large military helicopter&CloseCurlyDoubleQuote;, a very long clap of thunder&CloseCurlyDoubleQuote;, and &ldquo;a freight train coming&CloseCurlyDoubleQuote;.  In addition to strange sounds, a few people in the area saw strange things that at first they didn’t understand.  In the morning, Mr. Hawkins noticed that “something didn’t look right” on the flank of Grand Mesa at the head of West Salt Creek; at about the same time a neighbor noticed that the flow of water from West Salt Creek in an irrigation ditch was disrupted.  Mr. Hawkins drove up to a ridge and looked down to see trees moving along the east side of the head of the valley.
</p>

<p>
  Later in the evening just after the Bracco family heard the loud rumbling, they got a call from Mr. Hawkins saying he noticed what looked to him like a “massive slide” in the West Salt Creek Valley.  An emergency responder who arrived within minutes after Mrs. Bracco called 911 at 6:17pm observed that a tree sticking out of the toe of the landslide debris moved about 12 m (40 ft) downslope during the first hour he was there.
</p>
<p>Mr. Hawkins&CloseCurlyQuote; son, Wes, and Clancy and Danny Nichols had been investigating the disrupted irrigation when the massive avalanche occurred.  Emergency responders searched for the three missing people that evening and then resumed their search the next day until concerns about the risk from remaining slope instability halted their search. Wes, Clancy, and Danny have not been found.
</p>

<h3 class="clear">Collecting Evidence On the Ground, In the Air, and from Seismometers</h3>

<figure class="left" style="width:300px">
  <img src="images/scarp1.jpg" alt=""/>
  <figcaption>
  Photograph of the head of the West Salt Creek rock avalanche near the end of the 2015 spring snowmelt season. The sag pond was nearly full, with a water-level rise of less than 1 m needed for water to begin to spill over the rock-slide slump block. On May 25, 2014, movement of the rock-slide slump block mobilized the rock avalanche that traveled down the West Salt Creek valley. Rock falls and rock slides from headscarp are ongoing. View is to the northwest toward the town of Collbran, Colorado.  (Photo credit: Jeff Coe, June 7, 2015)
  </figcaption>
</figure>

<p>
  Scientists arrived on the scene 1 day after the May 25, 2014 event and began making field observations. In the first two weeks following the avalanche, they made measurements of the avalanche deposits and the amount of flow in West Salt Creek, and conducted an emergency hazard assessment for the Salt Creek and Plateau Creek valleys.  In addition to measurements in the first two weeks, the scientists spent an additional 10-person weeks in the field using high-resolution unmanned aircraft system (UAS, commonly known as drones) imagery and lidar to map and measure structures within the avalanche deposit.  They found that by 6 months after the event, many of the details they had been able to observe and measure initially had already degraded.
</p>
<p>
  Seismic recordings from 22 instruments were used to provide the timing of the avalanche sequence, the size of forces, and the direction of the forces.  This informed the scientists whether the avalanche was speeding up, slowing down, or going around a curve.  The precise clock in the instruments provided the timing for all of the observed motions in the recordings.  The use of seismic instruments to learn about landslides and avalanches was first used after the huge landslide caused by the Mount St. Helens eruption in 1980.  This technique has been gaining momentum during the last few years, but it only works when the landslide or avalanche is large and energetic enough to be recorded on distant seismometers.
</p>

<h3 class="clear">Science Tells the Story</h3>

