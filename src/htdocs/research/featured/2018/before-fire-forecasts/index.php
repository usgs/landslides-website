<p>
  To determine how well the new approach works, the scientists compared the prediction based on this method against a prediction using post-fire data analyses (the standard method). Then they went a step further and tested the accuracy of the prediction resulting from their method against a real-world situation during the winter of 2016-17 in Las Lomas Canyon in southern California.  They tested the new versus standard prediction results on 8 burned areas in 4 regions: 1) the San Gabriel and San Bernardino Mountains of southern California, 2) the southern Cascade Mountains of northern California, 3) the central Rocky Mountains of southern Colorado, and 4) the Chelan Mountains of the Cascade Range in central Washington.
</p>
<p>
  By looking at 8 diverse burned areas, they could compare their computed forecast using simulated fire data with that using the standard forecasting method with observed post-fire data.  They compared values for 3 parameters: the forecasted likelihood of a debris flow, the volume of material in the debris flow, and the amount of rain required to initiate a debris flow.  The agreement was good for the likelihood and volume, and bit less accurate for the rainfall amount for debris-flow initiation.
</p>
<p>
  Then they compared the predictions of the simulation method to actual observations of debris-flow occurrence (did it happen or not?). Their prediction for the occurrence, volume of material, and the amount of rainfall to initiate a debris flow agreed very well with what was observed.  In fact, the estimates using the simulated fire data were just as good as those based on the post-fire data.
</p>
<p>
  This approach allows a hazard estimation before a fire even occurs. This would allow communities the time needed to identify and prioritize areas in need of risk reduction, develop emergency management and response strategies, and implement mitigation projects to reduce the potential impact of debris flows. The presented method may be applicable throughout the world where there is plentiful information on the regional vegetation and historical fire severity.  It can also be modified to forecast other post-wildfire hazards such as flooding and erosion.
</p>

