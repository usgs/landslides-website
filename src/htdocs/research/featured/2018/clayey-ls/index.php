  During this time, they collected hourly measurements of rainfall, landslide movement, landslide thickness changes, groundwater pressure, and swell pressure (the pressure exerted when the soil in the landslide expands, usually due to the presence of water).  They also collected samples of the material (dirt) from the landslide to analyze in a laboratory.  From these samples they learned about the water content, the density, shear strength (how well the material holds together under pressure), type of landslide materials and minerals (most importantly the percentage of clay minerals), the shrink-swell amount, and the swell pressure.
</p>

<p>
  Previous studies concluded that pore water pressure, or the pressure of water in the open spaces between the dirt particles in the landslide material, controlled landslide movement. If that were the case, it follows that one would expect the landslide to start moving when it contained enough water, and to move the fastest when it contained the most water. However, the data in this study showed that the timing and speed of the earthflow movement did not correlate with the amount of water.   Instead, the initiation of movement was several months after the water amount peaked, and the peak speed occurred when the amount of water was decreasing.  The previously mentioned lab analyses of the samples from the earthflow were essential in illuminating the reasons for this behavior.
</p>

