<p>
  The earthflow material contained a large amount of expansive clay. Clay swells when it has more water and shrinks when there is less water.  The scientists hypothesized, from the correlation of the swell pressures measured in the laboratory and field with the earthflow movement, that the presence and amount of clay in the earthflow were connected to the timing and speed of the movements. When there was a large volume of water present, the clay expanded and exerted high pressure on the sides of the landslide, restricting the movement of the earthflow like when brakes are applied to a wheel. Then when the water volume decreased, the clay shrunk, decreasing the swell pressure and allowing the earthflow to accelerate.
</p>

<p>
  All steep slopes may look the same, but they don’t behave the same. Some steep slopes have the potential to fail suddenly and quickly and cause loss of life, and other steep slopes are more lethargic and pose a lower safety hazard. Many factors that cause earthflow acceleration also affect other types of landslides, including those that move rapidly, so the findings from this study may have wide applicability. The results will also help identify which slopes pose the highest hazards.
</p>

