<p>
  One might think that if there was already a landslide in a particular location that thereâ€™d be nothing left to make another landslide in the future. Regrettably that is not always the case. Indeed, in some geologic settings evidence of a preexisting landslide plays a role in the mapping of future landslide hazards. The deadliest individual landslides in the U.S. recently were in places where there had previously been a landslide. In the small beach community of La Conchita, CA, just south of Santa Barbara along Highway 101, a landslide occurred in 1995 followed by a debris flow in 2005, killing 10 people and damaging 36 homes.  In Oso, WA situated next to the North Fork of the Stillaguamish River about 50 miles SW of Seattle, a 2006 landslide was reactivated in 2014 as a debris-avalanche flow that killed 43 people and damaged private property and local highways.  A few months later a large rock avalanche near the remote town of Collbran, Colorado occurred from the location of a preexisting rockslide, resulting in the deaths of 3 people.  These are just a few examples of many repeat landslides that have been observed.
</p>

