<p>
  We usually hear about landslides and avalanches that are caused by large amounts of rainfall, the shaking from earthquakes, or a volcanic eruption, but we may be hearing more about avalanches caused by the (seemingly innocuous) melting of ice in the coming years. One such rock avalanche occurred on the Lamplugh glacier in a remote part of Glacier Bay National Park and Preserve in June of 2016. The occurrence was initially known only because it was captured on seismic instruments used to record earthquakes, and it released the same amount of energy as an M5.2 earthquake.
</p>
<p>
  Rock avalanches are landslides of fragmented rock that begin from rock slides and can be extremely hazardous because they are large and can move long distances at extremely rapid speeds.  Southeast Alaska, where Glacier Bay National Park is located, has had a notable number of rock avalanches in recent years, including the largest non-volcanic landslide ever recorded in North America in October of the previous year on the Tyndall Glacier. This rock avalanche generated a tsunami that ran about 600 ft up a shoreline, one of the largest tsunami runups ever recorded.
</p>
<figure>
<img src="images/map.gif" alt=""/>
<figcaption>
  Map of the Glacier Bay study area with the 24 rock avalanches in this study shown as red dots.
</figcaption>
</figure>
<p>
  What is happening in Glacier Bay National Park to suddenly cause more frequent and larger rock avalanches?  Three landslide scientists (from the USGS, CO School of Mines, and Ministry of Forests, Lands, Natural Resource Operations and Rural Development in Canada) examined 33 years of Landsat satellite images to try to find out.  They focused on a peninsular area in the western portion of the Park mostly covered with ice, with the Pacific Ocean on one side and Glacier Bay on the other. They identified rock avalanches on the images by 1) looking for areas with a high contrast to the surrounding ice and snow that would indicate rock avalanche material, 2) large differences between successive images, indicating a change in the topography, and 3) typical shapes indicative of rock avalanches. After identifying 24 landslides, they checked the earthquake record to make sure none of them were caused by earthquakes, since they were only interested in those that had other causes.  None had been caused by earthquakes.
</p>
<p>
  All of the landslides were in the northern part of the study area at high elevations, and 75% of them were on north- to northeast-facing slopes. Those areas are particularly subject to mountain permafrost.  Mountain permafrost is the ice in the cracks and crevices between the rocks that hold them together and help stabilize steep slopes.  There were 3 clusters of activity: 1984-1986, 1994-1995, and 2012-present, and the most recent cluster had more and larger rock avalanches that originated higher on the slopes and travelled further.
</p>
<figure>
<img src="images/Fig11-mod.gif" alt=""/>
<figcaption>
  Graph showing landslides that occurred when the elevation of the freezing temperature in the center of the northern half of the study area was below the average elevation (blue bars) and above the average elevation (red bars). The clusters are highlighted in yellow. (Temperature data is from the North American Freezing Level Tracker (NAFLT).) The question marks signify that the start and end times for the first and last clusters (respectively) are not known. The clusters in this study occured when the temperature on the upper slopes was higher than normal for successive years.
</figcaption>
</figure>
<p>
  After eliminating several other possible causes for the increase in numbers, rock size, and travel distance for the most recent avalanches, the scientists suggest that the melting of the mountain permafrost is the answer. The overall warmer temperatures in the last few decades in this part of Alaska cause the permafrost to degrade or partially melt, destabilizing the slopes and making rock slides and rock avalanches more likely.  The reason why the rock avalanches are initiating at higher elevations and on the north side of slopes is that those are areas that have more mountain permafrost and are most susceptible to changes in long-term temperatures.
</p>
<p>
  These observations and hypotheses can be compared with additional observations in the future to see if they hold up.  If indeed warmer temperatures cause destabilization of steep slopes in traditionally cold climate regions, there will be more high-risk areas for rock slides and rock avalanches in the future.
</p>
