<h2>Prior to Storms</h2>

<ol class="separator">
	<li>Watch the patterns of storm-water drainage near your home, and note the places where runoff water converges, increasing flow in channels.  These are areas to avoid during a storm.</li>
	<li>Contact your local authorities to learn about the emergency-response and evacuation plans for your area.  Develop your own emergency plan for your family or business.</li>
</ol>

<h2>During a Storm</h2>

<ol class="separator">
	<li>Stay alert! Many debris-flow and flood fatalities occur when people are sleeping.  Listen to the radio for warnings of intense rainfall.  NOAA Weather Radio All Hazards tone alert will let you know of hazards in your area.  Be aware that intense bursts of rain may be particularly dangerous, especially after longer periods of heavy rainfall.</li>
	<li>If you are in an area susceptible to flooding or debris flow (or has experienced flooding or debris flow in the past), consider leaving if it is safe to do so.  Remember that driving during heavy rainstorms can be hazardous.</li>
	<li>If you are near a stream or a channel, listen for any unusual sounds that might indicate moving debris, such as trees cracking or boulders knocking together.  A trickle of flowing mud or debris may precede larger flows.  Be alert for any sudden increases or decreases in water flow and for a change from clear to muddy water.  Such changes may indicate debris-flow activity upstream, so be prepared to move quickly.  Don't delay! Save yourself, not your belongings.</li>
	<li>Keep in mind that rises in water levels during flash floods and debris flows may occur much more rapidly, and may be significantly larger, than those produced when the watershed is not burned.</li>
	<li>Be particularly alert when driving.  Bridges may be washed out, and culverts overtopped.  Do not cross flooding streams!! Turn Around, Don't Drown &reg;!  Embankments along roadsides are particularly susceptible to landsliding.  Watch the road for collapsed pavement, mud, fallen rocks, and other indications of debris flow.</li>
</ol>

<p>
  Warnings and Watches for post-fire flash flood and debris flow are based on rainfall intensity-duration thresholds. Such thresholds have been developed for recently burned areas in southern California by comparing characteristics of storms known to have produced flash floods and debris flows with those that did not. Thresholds are defined by identifying those combinations of rainfall intensity and duration that are unique to flash flood and debris-flow producing storms. Where available, information on known times of flood or debris-flow occurrence is incorporated into the thresholds. Threshold lines delineate a range of rainfall combinations - from short duration, high intensity to longer duration, lower intensity - any of which can result in flash-flood or debris-flow activity. Post-fire threshold conditions change with time as sediment supplies are depleted and vegetation recovers, and so in addition to thresholds that are useful for immediately after a fire, thresholds for the following rainy season have also been developed.
</p>

