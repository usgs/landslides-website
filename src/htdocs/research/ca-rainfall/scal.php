<h2>Shallow Landslides and Debris Flows</h2>

<figure class="left" style="width:300px">
  <img src="images/santapaula2005.png" alt="photo of mountain with brown landslide"/>
  <figcaption>
    A large landslide that transformed into debris flow and recent smaller landslides west of Santa Paula, CA. These landslides were triggered by strong winter storms in 2005. (Photo by Mark Reid and Jonathan Godt, USGS)
  </figcaption>
</figure>
<p>
  Shallow landsides are generally less than (3-5 m) (10-15 ft) in depth and can transform into rapidly moving debris flows. Previous work at the USGS has identified both the areas of southwestern California most susceptible to shallow landslides and the rainfall conditions required to trigger slope failures.  Maps displaying where shallow landslides are most likely to occur are based on observation of previous landslide activity, topographic slope, and information on the bedrock material (see <a href="http://pubs.er.usgs.gov/publication/ofr0317">Preliminary soil-slip susceptibility maps, southwestern California</a>).
</p>
<p>
Shallow landslides can occur at any time during the winter, but are more likely happen when the ground is nearly saturated. In southern California, at least 25 cm (10 in) of rainfall during the winter is needed to nearly saturate the ground. After this point, a rain burst of 5-6 mm (0.2 to 0.25 in) in one hour has been observed to trigger abundant shallow landslides (see <a href="http://pubs.er.usgs.gov/publication/pp851">Soil slips, debris flows, and rainstorms in the Santa Monica Mountains and vicinity, southern California</a>).
</p>

