<h2>Recent Burned Areas</h2>

<figure class="right" style="width:300px">
  <img src="images/colbyfire2014.png" alt="photo of brown hillslope with no vegetation"/>
  <figcaption>
    2014 Colby Fire above Glendora, CA. This same area experienced widespread post-fire debris flows following a major winter storm in 1969. (Photo by Dennis Staley, USGS)
  </figcaption>
