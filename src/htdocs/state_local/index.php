    <h2>Landslide Emergency Management</h2>

    <p>Each locality usually has a unique protocol and procedure for landslide advisories, warnings, and potential evacuation.  Most cities and counties have information on their home websites or have departments of emergency management which can be contacted by phone.</p>

    <h2>Mitigation</h2>

    <p>Mitigation includes any activities that prevent an emergency, reduce the chance of an emergency happening, or lessen the damaging effects of unavoidable emergencies. Investing in preventive mitigation steps now such as planting ground cover (low growing plants) on slopes, or installing flexible pipe fitting to avoid gas or water leaks, will help reduce the impact of landslides and mudflows in the future. For more information on mitigation, contact your local emergency management office.</p>

