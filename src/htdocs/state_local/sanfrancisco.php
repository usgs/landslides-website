<h3>What happened during the last strong El Ni&ntilde;o in 1997-1998?</h3>

<p>Heavy rainfall associated with a strong El Ni&ntilde;o caused over $150 million in landslide damage in the 10-county San Francisco Bay region during the winter and spring of 1998. Scientists from the USGS Landslide Hazards Program based in Reston, Virginia; Golden, Colorado; and Menlo Park,
California; and from the USGS Geologic Mapping Program's San Francisco Bay Mapping Team based in Menlo Park, California, cooperated in the landslide-damage assessments. <a href="https://pubs.usgs.gov/mf/1999/mf-2325/">Click here to see the results.</a></p>

<p>The 1998 El Nino storm triggered several thousand small shallow landslides, known as debris flows, in hilly terrain east of San Francisco Bay south of the city of Oakland.  These potentially dangerous landslides were mapped in detail from aerial photographs and studied in order to better understand the hazard.</p>

<ul>
  <li><a href="https://pubs.usgs.gov/mf/2002/mf-2384/">Debris Flows Triggered by the El Ni&ntilde;o Rainstorm of February 2-3, 1998, Walpert Ridge and Vicinity, Alameda County, California</a></li>

  <li><a href="https://pubs.usgs.gov/sim/2004/2859/">Map showing recent (1997-98 El Ni&ntilde;o) and historical landslides, Crow Creek and vicinity, Alameda and Contra Costa Counties, California</a></li>
</ul>

<h3>Was the February 1998 El Ni&ntilde;o storm a unique event?</h3>

<p>
  Severely destructive winter rainstorms in Central California are infrequent, but can be counted on to reoccur.
</p>

<p>
  For example, a catastrophic 32-hour storm triggered landslides and floods throughout the San Francisco Bay area in Januray 1982. More than 18,000 debris flows damaged over 100 homes and killed 14 residents; one large landslide caused 10 additional fatalities. Direct costs from damage by slope failure alone exceeded $66 million.  An event of similar magnitude occurred in the winter of 1955.
</p>

<ul>
  <li><a href="https://pubs.usgs.gov/pp/1988/1434/">Landslides, Floods, and Marine Effects of the Storm of January 3-5, 1982, in the San Francisco Bay Region, California</a></li>
</ul>

