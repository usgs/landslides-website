<h3>Landslide Folio</h3>

<p>The San Francisco Bay Region Landslide Folio comprises six separate but related reports.  These were originally prepared in anticipation of damaging Bay area storms during the 1997-1998 El Ni&ntilde;o, but are relavent for current and future storm seasons (whether during or outside of an El Ni&ntilde;o season) as well; the reports are keyed by letter to  <a href="https://pubs.usgs.gov/of/1997/of97-745/">Open-File Report 97-745</a>. These reports contain digital maps of topography, locations of existing and possible future landslides, and rainfall amounts needed to trigger shallow landsliding. The data contained within these reports covers the entire region, as well as each county individually. This information is useful for responding to possible slope failure within the San Francisco Bay Area, California.</p>

