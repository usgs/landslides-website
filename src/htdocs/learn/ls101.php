<h2>What is a Landslide?</h2>

<figure class="right">
  <img src="images/ls101.gif" alt="landslide sketch"/>
</figure>

<p>
  <strong>The term landslide includes a wide range of ground movement, such as rock falls, deep failure of slopes, and shallow debris flows.</strong> Although gravity acting on an over-steepened slope is the primary reason for a landslide, there are other contributing factors:
</p>

<ul>
  <li>erosion by rivers, glaciers, or ocean waves create oversteepened slopes</li>
  <li>rock and soil slopes are weakened through saturation by snowmelt or heavy rains</li>
  <li>earthquakes create stresses that make weak slopes fail</li>
  <li>earthquakes of magnitude 4.0 and greater have been known to trigger landslides</li>
  <li>volcanic eruptions produce loose ash deposits, heavy rain, and debris flows</li>
  <li>excess weight from accumulation of rain or snow, stockpiling of rock or ore, from waste piles, or from man-made structures may stress weak slopes to failure and other structures</li>
</ul>

<p>
  Slope material that become saturated with water may develop a debris flow or mud flow. The resulting slurry of rock and mud may pick up trees, houses, and cars, thus blocking bridges and tributaries causing flooding along its path.
</p>

<h2>Where do Landslides Occur?</h2>

<p>
  <strong>Landslides occur in every state and U.S. territory.</strong> The Appalachian Mountains, the Rocky Mountains and the Pacific Coastal Ranges and some parts of Alaska and Hawaii have severe landslide problems. Any area composed of very weak or fractured materials resting on a steep slope can and will likely experience landslides.
</p>

<p>
  Although the physical cause of many landslides cannot be removed, geologic investigations, good engineering practices, and effective enforcement of land-use management regulations can reduce landslide hazards.
</p>

<p>
  USGS scientists continue to produce landslide susceptibility maps for many areas in the United States. In every state, USGS scientists monitor streamflow, noting changes in sediment load carried by rivers and streams that may result from landslides. Hydrologists with expertise in debris and mud flows are studying these hazards in volcanic regions.
</p>

