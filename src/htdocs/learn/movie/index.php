<h2>Riding The Storm - Landslide Danger in the San Francisco Bay Area</h2>
<div class="row">
  <div class="column three-of-five">
    <p>
    <ul>
      <li> A catastrophic 1982 rainstorm triggered 18,000 landslides in the Bay Area, claiming 25 lives and causing $66 million in property damage </li>
      <li> The combination of steep slopes, weak rocks, and intense winter storms make Bay Area uplands an ideal setting for landslides </li>
      <li> Landslides include both swift, potentially deadly debris flows and slower, but destructive deepseated slides </li>
      <li> Learn what USGS scientists have discovered about landslide dynamics and which slopes are most susceptible to sliding </li>
      <li> Hear the devastating stories of Bay Area residents affected by landslides and learn to recognize the danger signs </li>
    </ul>
