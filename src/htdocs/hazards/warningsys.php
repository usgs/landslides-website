    <p>
    Just a short period of moderate rainfall on a burn scar can lead to flash floods and debris flows.  Rainfall that is normally absorbed by vegetation can run off almost instantly.  This causes creeks and drainage areas to flood much sooner during a storm, and with more water, than normal.  Additionally, the soils in a burn scar are highly erodible so flood waters can contain significant amounts of mud, boulders, and vegetation.  The powerful force of rushing water, soil, and rock, both within the burned area and downstream, can destroy culverts, bridges, roadways, and structures, and can cause injury or death if care is not taken.</p>


    <h2>About the Warning System</h2>

    <figure class="right" style="width:250px;clear:right;">
