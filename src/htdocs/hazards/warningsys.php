    <p>The National Oceanic and Atmospheric Administration (NOAA) and the United States Geological Survey (USGS) have established a demonstration flash-flood and debris-flow early-warning system for recently burned areas in southern California.  The demonstration project covers eight counties within southern California, and utilizes the National Weather Service's (NWS) Flash Flood Monitoring and Prediction (FFMP) system. FFMP identifies when both flash floods and debris flows are likely to occur based on comparisons between radar precipitation estimates and established rainfall intensity-duration threshold values.  Beginning in autumn 2005, advisory Outlooks, Watches and Warnings are disseminated to emergency management personnel through the NWS Advanced Weather Information Processing System.</p>
